-- 1. Tạo Database
CREATE DATABASE IF NOT EXISTS db_linhkienmaytinh CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE db_linhkienmaytinh;

-- 2. Tạo các bảng cơ bản (đã rút gọn cho đúng cấu trúc của bạn)
CREATE TABLE BRAND (BRAND_ID BIGINT AUTO_INCREMENT PRIMARY KEY, NAME VARCHAR(100) NOT NULL UNIQUE);
CREATE TABLE CATEGORY (CATEGORY_ID BIGINT AUTO_INCREMENT PRIMARY KEY, NAME VARCHAR(120) NOT NULL UNIQUE);

CREATE TABLE PRODUCT (
    PRODUCT_ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    SKU VARCHAR(64) NOT NULL UNIQUE,
    NAME VARCHAR(255) NOT NULL,
    BRAND_ID BIGINT,
    CATEGORY_ID BIGINT,
    LIST_PRICE DECIMAL(12,2) NOT NULL DEFAULT 0,
    WARRANTY_MONTHS INT NOT NULL DEFAULT 12,
    FOREIGN KEY (BRAND_ID) REFERENCES BRAND(BRAND_ID),
    FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY(CATEGORY_ID)
);

